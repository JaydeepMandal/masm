.MODEL SMALL
.STACK 300H
.DATA

ARRAY DW 100 DUP(30H)
INP DB 'ENTER ARRAY: $'
MSG1 DB "GREATEST ELEMENT - $"
MSG2 DB "SMALLEST ELEMENT - $"
NUMMIN DW ?
NUMMAX DW ?
LBK    DB 13,10,'$'   ;LINE BREAK.
RES DB 10 DUP('$')


PRINT MACRO MSG
MOV AH, 09H
LEA DX, MSG
INT 21H
;INT 3
ENDM

.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX

START:

LEA SI, ARRAY

CALL INPUTARRAY
MOV CX, SI
LEA SI, ARRAY
SUB CX, SI
SAR CX, 1

MOV BX,[SI]
MOV NUMMIN, BX
MOV NUMMAX, BX

LOOP3: 
	MOV BX,[SI]
	CMP NUMMAX, BX
	JG IF11
	MOV NUMMAX,BX
IF11: 
	CMP NUMMIN, BX
	JL IF22
	MOV NUMMIN, BX
IF22: 
	INC SI
	INC SI
	LOOP LOOP3

;MOV NUMMAX,AH
;MOV NUMMIN,AL

PRINT MSG1
MOV AX, NUMMAX
MOV AH,00H
LEA SI,RES
CALL HEX2DEC
LEA DX,RES	;DISPLAY THE STRING OF NUMBERS FROM HEX
MOV AH,9
INT 21H

MOV DL,10
MOV AH,02H
INT 21H
MOV DL,13
MOV AH,02H
INT 21H

PRINT MSG2
MOV AX,NUMMIN
MOV AH,00H
LEA SI,RES
CALL HEX2DEC
LEA DX,RES	;DISPLAY THE STRING OF NUMBERS FROM HEX
MOV AH,9
INT 21H

COMMENT @
	MOV AH,0
	MOV AL,NUMMIN
	LEA SI,RES
    CALL HEX2DEC
    LEA DX,RES	;DISPLAY THE STRING OF NUMBERS FROM HEX
    MOV AH,9
    INT 21H
	@

MOV AH,4CH
INT 21H

MAIN ENDP

HEX2DEC PROC NEAR	;CONVERSION
        MOV CX,0
        MOV BX,10

LOOP1: MOV DX,0
        DIV BX
        ADD DL,30H	;FOR CHARACTER
        PUSH DX
        INC CX
        CMP AX,9
        JG LOOP1

        ADD AL,30H
        MOV [SI],AL

LOOP2: POP AX
        INC SI
        MOV [SI],AL
        LOOP LOOP2
        RET
HEX2DEC ENDP

GETNUM PROC
	PUSH CX
	MOV BX, 0
	MOV AL,30H
	LOOPGETNUM:
		SUB AL, 30H
		MOV CH,AL
		MOV AX,BX
		MOV CL, 0AH
		MUL CL
		MOV CL,CH
		MOV CH, 0
		ADD AX,CX
		MOV BX,AX
		MOV AH, 1
		INT 21H
		CMP AL, 0DH ;ENTER KEY PRESSED, EXIT
		JE OUTSIDE
		CMP AL, 20H
		JE OUTSIDE
		JMP LOOPGETNUM
	OUTSIDE:
	POP CX
	RET
GETNUM ENDP

INPUTARRAY PROC
	MOV AH, 09H
	LEA DX, INP
	INT 21H
	MOV CX, 100
	LOOPINP:
		CALL GETNUM
		MOV [SI], BX
		INC SI
		INC SI
		CMP AL, 0DH
		JE HEREARRAY
	LOOP LOOPINP
	HEREARRAY:
	RET
INPUTARRAY ENDP

END MAIN

